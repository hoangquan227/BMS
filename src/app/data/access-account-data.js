export function getCustomerAccounts(customerId) {
  return accessAccounts.filter((account) => account.customerId === customerId);
}

export function findAccessCustomer(customerId) {
  return accessCustomers.find((customer) => customer.id === customerId) || accessCustomers[0];
}

export function findAccessAccount(accountId) {
  return accessAccounts.find((account) => account.id === accountId);
}

export function getCustomerAccountSummary(customer) {
  const accounts = getCustomerAccounts(customer.id);
  const importantTypes = ["Thuế điện tử", "Bảo hiểm xã hội", "Hóa đơn điện tử"];
  const missing = importantTypes.filter((type) => !accounts.some((account) => account.type === type));

  return {
    ...customer,
    accountCount: accounts.length,
    missingImportantAccounts: missing,
  };
}
